#include "include.h"

//-------------------------------------------------------------------------*
//函数名: KEY_Init（）                                                      *
//功  能: 初始化IO口                                                      * 
//参  数:                                                                *
//返  回: 无                                                              *
//简  例: KEY_Init（）                                                     *
//-------------------------------------------------------- -----------------*
void KEY_Init(void)
{
   GPIO_Init(PORTB,20,GPI,1);
   GPIO_Init(PORTB,21,GPI,1);
   GPIO_Init(PORTB,22,GPI,1);    
}

//-------------------------------------------------------------------------*
//函数名: KEY_Read（）                                                      *
//功  能: 读取按键值                                                       * 
//参  数:                                                                *
//返  回:                                                              *
//简  例: KEY_Read( KEY0)                                                *
//-------------------------------------------------------------------------*
u8 KEY_Read(KEYn_e keyno)
{
    switch(keyno) 
    {
      case KEY0:
        return GPIO_Get(PTB20);
      break;
      
      case KEY1:
        return GPIO_Get(PTB21);
      break;
      
      case KEY2:
        return GPIO_Get(PTB22);
      break;
      default:
        return 0XFF;
           
    }
}


